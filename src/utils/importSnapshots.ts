function buildSnapshots(): DailySnapshot[] {
  const snapshots: DailySnapshot[] = [];

  for (let i = 0; i < rawData.length; i++) {
    const [date, dividend, totalAsset, assetChange] = rawData[i];
    const half = Math.round(totalAsset / 2);

    // changeRate = assetChange / previousDayTotalAsset * 100
    let changeRate = 0;
    if (i > 0 && assetChange !== 0) {
      const prevTotal = rawData[i - 1][2];
      changeRate = Math.round((assetChange / prevTotal) * 10000) / 100;
    }

    snapshots.push({
      date,
      totalAsset,
      wifeAsset: half,
      husbandAsset: totalAsset - half,
      assetChange,
      changeRate,
      dividend: dividend || undefined,
    });
  }

  return snapshots;
}

const SNAPSHOT_KEY = 'asset_snapshots';
const IMPORT_FLAG = 'snapshots_imported_v2';

export async function importHistoricalSnapshots(): Promise<void> {
  if (localStorage.getItem(IMPORT_FLAG)) {
    return;
  }

  const historicalSnapshots = buildSnapshots();

  // Merge with any existing snapshots in localStorage
  const raw = localStorage.getItem(SNAPSHOT_KEY);
  const existing: DailySnapshot[] = raw ? JSON.parse(raw) : [];

  // Build a map keyed by date, historical data overwrites existing for this import
  const map = new Map<string, DailySnapshot>();
  for (const s of existing) {
    if (s && s.date) {
      map.set(s.date, s);
    }
  }
  for (const s of historicalSnapshots) {
    map.set(s.date, s);
  }

  const merged = Array.from(map.values()).sort((a, b) => b.date.localeCompare(a.date));
  localStorage.setItem(SNAPSHOT_KEY, JSON.stringify(merged));
  localStorage.setItem(IMPORT_FLAG, 'true');

  console.log(`Imported ${historicalSnapshots.length} historical snapshots to localStorage (${merged.length} total)`);
}
